import type { AgentManifest } from "../types/agent";

/** Shared defaults to keep manifests DRY */
const defaults = {
  version: "1.0.0",
  api_version: "agent/v1",
  memory: {
    enabled: true,
    short_term: {
      backend: "chromadb",
      collection: "agent_scratchpad",
      ttl_minutes: 60,
    },
    long_term: { backend: "chromadb", collection: "agent_memory" },
    shared: { read: ["shared_knowledge"], write: ["shared_knowledge"] },
    retrieval: {
      semantic_weight: 0.7,
      recency_weight: 0.2,
      importance_weight: 0.1,
      default_k: 5,
      confidence_threshold: 0.6,
    },
  },
  health: {
    readiness: { type: "ping", interval_seconds: 30, timeout_seconds: 5 },
    liveness: { type: "heartbeat", interval_seconds: 60 },
  },
  execution: {
    isolation: "subprocess" as const,
    docker_image: null,
    working_dir: "./workspace",
    env_required: [],
    env_optional: [],
  },
  resource_limits: {
    max_execution_time_seconds: 300,
    max_tool_iterations: 15,
    max_tokens_per_turn: 8192,
    memory_limit_mb: 512,
  },
};

function agent(
  id: string,
  name: string,
  description: string,
  icon: string,
  colorClass: string,
  colorHex: string,
  tags: string[],
  role: string,
  goal: string,
  modelId: string,
  payloadType: string,
  tools: string[] = [],
  overrides?: Partial<AgentManifest>,
): AgentManifest {
  return {
    id,
    ...defaults,
    display: {
      name,
      description,
      icon,
      color_class: colorClass,
      color_hex: colorHex,
      tags,
    },
    soul: {
      path: `./SOUL.md`,
      role,
      goal,
      constraints: ["Follow THESIS.md directives"],
    },
    model: {
      preferred: {
        provider: "openrouter",
        model_id: modelId,
        temperature: 0.7,
        max_tokens: 4096,
      },
      fallback_chain: [
        {
          provider: "anthropic",
          model_id: "claude-sonnet-4.6",
          temperature: 0.7,
          max_tokens: 4096,
        },
      ],
    },
    tools: tools.map((t) => ({
      id: t,
      type: "builtin" as const,
      enabled: true,
      permissions: {},
    })),
    schemas: { input: {}, output: {}, payload_type: payloadType },
    ...overrides,
  };
}

